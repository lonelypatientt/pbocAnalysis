package com.caafc.pbocAnalysis.util;

/**
 * @author xihn
 * 将人行征信报告中的婚姻状况装换为key
 */
public class ChangeMaritalState {

	public static String maritalState(String maritalState){
		if(maritalState.equals("未婚")){
			return "10";
		}else if(maritalState.equals("已婚")){
			return "20";
		}else if(maritalState.equals("丧偶")){
			return "30";
		}else if(maritalState.equals("离婚")){
			return "40";
		}else if(maritalState.equals("未知")){
			return "90";
		}
		return "";
	}
}
