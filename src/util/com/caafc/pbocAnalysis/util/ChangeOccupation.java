package com.caafc.pbocAnalysis.util;

/**
 * 
 * @author xihn 职业转换
 */
public class ChangeOccupation {
	public static String occupation(String s) {
		if (s.equals("国家机关、党群组织、企业、事业单位负责人")) {
			return "01";
		} else if (s.equals("专业技术人员")) {
			return "29";
		} else if (s.equals("办事人员和有关人员")) {
			return "34";
		} else if (s.equals("商业、服务业人员")) {
			return "48";
		} else if (s.equals("农、林、牧、渔、水利业生产人员")) {
			return "56";
		} else if (s.equals("生产、运输设备操作人员及有关人员")) {
			return "94";
		} else if (s.equals("军人")) {
			return "X0";
		} else if (s.equals("不便分类的其它从业人员")) {
			return "Y0";
		} else if (s.equals("未知")) {
			return "9";
		}
		return "";
	}
}
