package com.caafc.pbocAnalysis.util;

/**
 * @author xihn
 * 人行征信客户性别装换为key
 */
public class ChangeGender {
	public static String gender(String gender){
		if(gender.equals("男性")){
			return "1";
		}else if(gender.equals("女性")){
			return "2";
		}else if(gender.equals("未知")){
			return "9";
		}
		return "";
	}
}
