package com.caafc.pbocAnalysis.util;

/**
 * @author xihn
 * 结案方式转换
 */
public class ChangeCloseType {
	public static String closeType(String s) {
		if (s.equals("判决")) {
			return "1";
		} else if (s.equals("调解")) {
			return "2";
		} else if (s.equals("其他")) {
			return "3";
		}  
		return "";
	}
}
