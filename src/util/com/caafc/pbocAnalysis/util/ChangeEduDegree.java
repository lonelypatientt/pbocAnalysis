package com.caafc.pbocAnalysis.util;

/**
 * @author xihn
 * 学位
 */
public class ChangeEduDegree {
	
	public static String eduDegree(String s) {
		if (s.equals("名誉博士")) {
			return "04";
		} else if (s.equals("博士")) {
			return "03";
		} else if (s.equals("硕士")) {
			return "02";
		} else if (s.equals("学士")) {
			return "01";
		} else if (s.equals("其他")) {
			return "99";
		} else if (s.equals("未知")) {
			return "9";
		} 
		return "";
	}
}
