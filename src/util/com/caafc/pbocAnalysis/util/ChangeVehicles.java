/**
 * @author xihn 车辆类型、使用性质、车辆状态、抵押标记
 */
public class ChangeVehicles {
	public static String carType(String s) {
		if (s.equals("半挂车")) {
			return "B";
		} else if (s.equals("电车")) {
			return "D";
		} else if (s.equals("全挂车")) {
			return "G";
		} else if (s.equals("货车")) {
			return "H";
		} else if (s.equals("轮式机械")) {
			return "J";
		} else if (s.equals("客车")) {
			return "K";
		} else if (s.equals("摩托车")) {
			return "M";
		} else if (s.equals("三轮汽车")) {
			return "N";
		} else if (s.equals("半挂牵引车")) {
			return "Q";
		} else if (s.equals("拖拉机代码")) {
			return "T";
		} else if (s.equals("专项作业车")) {
			return "Z";
		} else if (s.equals("其它")) {
			return "X";
		}
		return "";
	}

	public static String useCharacter(String s) {
		if (s.equals("非营运")) {
			return "A";
		} else if (s.equals("公路客运")) {
			return "B";
		} else if (s.equals("公交客运")) {
			return "C";
		} else if (s.equals("出租客运")) {
			return "D";
		} else if (s.equals("旅游客运")) {
			return "E";
		} else if (s.equals("货运")) {
			return "F";
		} else if (s.equals("租赁")) {
			return "G";
		} else if (s.equals("警用")) {
			return "H";
		} else if (s.equals("消防")) {
			return "I";
		} else if (s.equals("救护")) {
			return "J";
		} else if (s.equals("工程抢险")) {
			return "K";
		} else if (s.equals("营转非")) {
			return "L";
		} else if (s.equals("出租转非")) {
			return "M";
		} else if (s.equals("教练")) {
			return "N";
		} else if (s.equals("其它")) {
			return "Z";
		}
		return "";
	}

	public static String carState(String s) {
		if (s.equals("正常")) {
			return "A";
		} else if (s.equals("转出")) {
			return "B";
		} else if (s.equals("被盗抢")) {
			return "C";
		} else if (s.equals("停驶")) {
			return "D";
		} else if (s.equals("注销")) {
			return "E";
		} else if (s.equals("违法未处理")) {
			return "G";
		} else if (s.equals("海关监管")) {
			return "H";
		} else if (s.equals("事故未处理")) {
			return "I";
		} else if (s.equals("嫌疑车")) {
			return "J";
		} else if (s.equals("查封")) {
			return "K";
		} else if (s.equals("暂扣")) {
			return "L";
		} else if (s.equals("强制注销")) {
			return "M";
		} else if (s.equals("事故逃逸")) {
			return "N";
		} else if (s.equals("锁定")) {
			return "O";
		} else if (s.equals("其它")) {
			return "Z";
		}
		return "";
	}
	
	public static String mortgageFlag(String s) {
		if (s.equals("未抵押")) {
			return "0";
		} else if (s.equals("已抵押")) {
			return "1";
		} 
		return "";
	}

}
