package com.caafc.pbocAnalysis.util;

/**
 * @author MyUser 行业转换
 */
public class ChangeEmployerProfession {
	public static String employerProfession(String s) {
		if (s.equals("农、林、牧、渔业")) {
			return "A";
		} else if (s.equals("采掘业")) {
			return "B";
		} else if (s.equals("制造业")) {
			return "C";
		} else if (s.equals("电力、煤气及水的生产和供应业")) {
			return "D";
		} else if (s.equals("建筑业")) {
			return "E";
		} else if (s.equals("交通运输、仓储和邮政业")) {
			return "G";
		} else if (s.equals("信息传输、计算机服务和软件业")) {
			return "I";
		} else if (s.equals("批发和零售业")) {
			return "F";
		} else if (s.equals("住宿和餐饮业")) {
			return "H";
		} else if (s.equals("金融业")) {
			return "J";
		} else if (s.equals("房地产业")) {
			return "K";
		} else if (s.equals("租赁和商务服务业")) {
			return "L";
		} else if (s.equals("科学研究、技术服务业和地质勘察业")) {
			return "M";
		} else if (s.equals("水利、环境和公共设施管理业")) {
			return "N";
		} else if (s.equals("居民服务和其他服务业")) {
			return "O";
		} else if (s.equals("教育")) {
			return "P";
		} else if (s.equals("卫生、社会保障和社会福利业")) {
			return "Q";
		} else if (s.equals("文化、体育和娱乐业")) {
			return "R";
		} else if (s.equals("公共管理和社会组织")) {
			return "S";
		} else if (s.equals("国际组织")) {
			return "T";
		} else if (s.equals("未知")) {
			return "Z";
		}
		return "";
	}
}
