package com.caafc.pbocAnalysis.menu;

import java.util.List;

public class MenuInfo {
	/**
	 * 编号
	 */
	private String meuIdt;
	/**
	 * 名称
	 */
	private String meuNam;
	/**
	 * url
	 */
	private String meuUrl;
	/**
	 * 父编号
	 */
	private String meuPid;
	/**
	 * 菜单的级数
	 */
	private String level;
	
	/**
	 * 菜单图片
	 */
	private String image;
	/**
	 * 菜单描述
	 */
	private String meuDes;
	
	/**
	 * 子菜单
	 */
	private List<MenuInfo> childMenuList;
	
	/**
	 * @author HLRain
	 * @version:2012-11-20
	 * 快捷菜单标识
	 */
	private String delFlg;
	
	/**
	 * @return the meuIdt
	 */
	public String getMeuIdt() {
		return meuIdt;
	}
	/**
	 * @param meuIdt the meuIdt to set
	 */
	public void setMeuIdt(String meuIdt) {
		this.meuIdt = meuIdt;
	}
	/**
	 * @return the meuNam
	 */
	public String getMeuNam() {
		return meuNam;
	}
	/**
	 * @param meuNam the meuNam to set
	 */
	public void setMeuNam(String meuNam) {
		this.meuNam = meuNam;
	}
	/**
	 * @return the meuUrl
	 */
	public String getMeuUrl() {
		return meuUrl;
	}
	/**
	 * @param meuUrl the meuUrl to set
	 */
	public void setMeuUrl(String meuUrl) {
		this.meuUrl = meuUrl;
	}
	/**
	 * @return the meuPid
	 */
	public String getMeuPid() {
		return meuPid;
	}
	/**
	 * @param meuPid the meuPid to set
	 */
	public void setMeuPid(String meuPid) {
		this.meuPid = meuPid;
	}
	/**
	 * @return the level
	 */
	public String getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(String level) {
		this.level = level;
	}
	/**
	 * @return the meuDes
	 */
	public String getMeuDes() {
		return meuDes;
	}
	/**
	 * @param meuDes the meuDes to set
	 */
	public void setMeuDes(String meuDes) {
		this.meuDes = meuDes;
	}
	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}
	/**
	 * @param image the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}
	/**
	 * @return the childMenuList
	 */
	public List<MenuInfo> getChildMenuList() {
		return childMenuList;
	}
	/**
	 * @param childMenuList the childMenuList to set
	 */
	public void setChildMenuList(List<MenuInfo> childMenuList) {
		this.childMenuList = childMenuList;
	}
	/**
	 * @return the delFlg
	 */
	public String getDelFlg() {
		return delFlg;
	}
	/**
	 * @param delFlg the delFlg to set
	 */
	public void setDelFlg(String delFlg) {
		this.delFlg = delFlg;
	}
	
}
